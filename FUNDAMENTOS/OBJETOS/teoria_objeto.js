/*
Bem como o array, um objeto é uma estrutura que armazena varias infomrações, porem ao invez de [],
ultilizamos de {}.
Alem disso, um objeto não é uma estrutura indexada como o array, que possui indices a partir de zero.
Na estrutura do objeto temos pares formados por chave e um determinado valor para aquela chave.
CHAVE: VALOR,
            estes sao membros do objeto, seja atributo ou metodo.
CHAVE: VALOR;
As chaves podem se repetir no mesmo objeto, porém quando feito, o valor que estiver mais abaixo sera 
o valor considerado.
Bem como em  um objeto na vida real, o objeto na programação tambem possui atributos e metodos ou funções...
Os atributos temos como as caracteristicas do objeeto e os metodos, suas funções ou ações...
Obs: Para qualquer caracteristica que precise ser utilizada dentro de uma ação dentro do proprio objeto
devems utilizar uma nomenclatura que indique que esta se referenciando a uma caracteristica do proprio objeto
e essa palavra em JS é this.
*/